package com.kenny.craftix.utils;

import static java.lang.System.out;

public class SimplePrint 
{
	
	public static void print(Object o)
	{
		out.println(o);
	}
	
	/**
	 * Prints a String and then terminate the line.
	 */
	public static void print(String s)
	{
		out.println(s);
	}

	/**
	 * Prints a byte int and then terminate the line.
	 */
	public static void print(byte b)
	{
		out.println(b);
	}
	
	/**
	 * Prints a short int and then terminate the line.
	 */
	public static void print(short sh)
	{
		out.println(sh);
	}
	
	/**
	 * Prints a int and then terminate the line.
	 */
	public static void print(int i)
	{
		out.println(i);
	}
	
	/**
	 * Prints a long int and then terminate the line.
	 */
	public static void print(long l)
	{
		out.println(l);
	}
	
	/**
	 * Prints a float and then terminate the line.
	 */
	public static void print(float f)
	{
		out.println(f);
	}
	
	/**
	 * Prints a double float and then terminate the line.
	 */
	public static void print(double d)
	{
		out.println(d);
	}
	
	/**
	 * Prints a char and then terminate the line.
	 */
	public static void print(char c)
	{
		out.println(c);
	}
	
	/**
	 * Prints a String just on one line.
	 */
	public static void printOnLine(String s)
	{
		out.print(s);
	}
	
	/**
	 * Prints a byte int just on one line.
	 */
	public static void printOnLine(byte b)
	{
		out.print(b);
	}
	
	/**
	 * Prints a short int just on one line.
	 */
	public static void printOnLine(short sh)
	{
		out.print(sh);
	}
	
	/**
	 * Prints a int just on one line.
	 */
	public static void printOnLine(int i)
	{
		out.print(i);
	}
	
	/**
	 * Prints a long int just on one line.
	 */
	public static void printOnLine(long l)
	{
		out.print(l);
	}
	
	/**
	 * Prints a float just on one line.
	 */
	public static void printOnLine(float f)
	{
		out.print(f);
	}
	
	/**
	 * Prints a double float just on one line.
	 */
	public static void printOnLine(double d)
	{
		out.print(d);
	}
	
	/**
	 * Prints a char just on one line.
	 */
	public static void printOnLine(char c)
	{
		out.print(c);
	}
}
